CREATE TABLE FUNCIONARIO (
  ID INT NOT NULL AUTO_INCREMENT,
  NOME VARCHAR(40),
  CPF CHAR(11),
  SENHA VARCHAR(20),
  PRIMARY KEY (ID)
);



CREATE TABLE CLIENTE (
  CPF CHAR(11) NOT NULL,
  NOME VARCHAR(40),
  PRIMARY KEY (CPF)
);

CREATE TABLE FORNECEDOR (
  CNPJ CHAR(15),
  NOME VARCHAR(40),
  PRIMARY KEY (CNPJ)
);

CREATE TABLE PRODUTO (
  CODPROD INT,
  NOMEPROD VARCHAR(20),
  QTDPROD INT,
  DESCRPROD VARCHAR(40),
  PRIMARY KEY (CODPROD)
);

CREATE TABLE PEDIDO (
  NUMPEDIDO INT,
  DATAPEDIDO DATE,
  PRIMARY KEY (NUMPEDIDO)
);

CREATE TABLE ITEMPEDIDO (
  CODPROD INT,
  NOMEPROD VARCHAR(20),
  QTDPROD INT,
  PRIMARY KEY (CODPROD)
);

